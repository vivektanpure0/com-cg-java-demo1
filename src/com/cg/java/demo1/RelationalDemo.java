package com.cg.java.demo1;

public class RelationalDemo {
	public static void main(String[] args) {
int num1 = 10, num2 = 20;
		
		// (salaryOffered < salaryExpected)  
		
		System.out.println(num1 == num2); // false
		System.out.println(num1 != num2); // true
		System.out.println(num1 > num2); // false
		System.out.println(num1 >= num2); // false
		System.out.println(num1 < num2); // true
		System.out.println(num1 <= num2); // true

		
	}
	
		
}
